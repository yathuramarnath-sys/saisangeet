// ─── Build areas from outlet tables (like POS does) ──────────────────────────

function buildAreasFromOutlet(outlet) {
  if (!outlet?.tables?.length) return null;
  const areaNames = [...new Set(outlet.tables.map((t) => t.workArea || t.area_name).filter(Boolean))];
  if (!areaNames.length) areaNames.push("Main");
  return areaNames.map((areaName) => {
    const tables = outlet.tables
      .filter((t) => (t.workArea || t.area_name || "Main") === areaName)
      .map((t) => ({
        id:     t.id,
        number: t.table_number || t.tableNumber || t.name,
        seats:  t.seats || 4,
      }));
    return {
      id:     `area-${areaName.toLowerCase().replace(/\s+/g, "-")}`,
      name:   areaName,
      tables,
    };
  });
}

// ─── Branch Config (localStorage) ────────────────────────────────────────────

const CAPTAIN_LS_KEY = "captain_branch_config";

function loadCaptainBranchConfig() {
  try { return JSON.parse(localStorage.getItem(CAPTAIN_LS_KEY) || "null"); }
  catch { return null; }
}
function saveCaptainBranchConfig(cfg) {
  localStorage.setItem(CAPTAIN_LS_KEY, JSON.stringify(cfg));
}
function clearCaptainBranchConfig() {
  localStorage.removeItem(CAPTAIN_LS_KEY);
}

// ─── Branch Setup Screen ──────────────────────────────────────────────────────

function CaptainBranchSetupScreen({ onComplete }) {
  const [code,     setCode]     = useState("");
  const [status,   setStatus]   = useState("idle");
  const [result,   setResult]   = useState(null);
  const [errorMsg, setErrorMsg] = useState("");

  async function handleVerify(e) {
    e.preventDefault();
    const trimmed = code.trim().toUpperCase();
    if (!trimmed) return;
    setStatus("loading");
    setErrorMsg("");
    try {
      const data = await api.post("/devices/resolve-link-code", {
        linkCode:   trimmed,
        deviceType: "Captain App",
      });
      setResult(data);
      setStatus("success");
    } catch (err) {
      setStatus("error");
      setErrorMsg(err?.message || "Invalid link code — check with your manager.");
    }
  }

  function handleConfirm() {
    const config = {
      outletId:     result.outletId,
      outletCode:   result.outletCode,
      outletName:   result.outletName,
      staff:        result.staff || [],
      configuredAt: new Date().toISOString(),
    };
    saveCaptainBranchConfig(config);
    // Save device token so all API calls use the correct tenant
    if (result.deviceToken) {
      localStorage.setItem("captain_token", result.deviceToken);
    }
    onComplete(config);
  }

  return (
    <div className="branch-setup-screen">
      <div className="branch-setup-card">
        <div className="branch-setup-logo" style={{ fontSize: 40 }}>C</div>
        <h1 className="branch-setup-title">Captain App</h1>
        <p className="branch-setup-subtitle">Device Setup</p>

        {status !== "success" ? (
          <>
            <p className="branch-setup-instruction">
              Enter the branch link code from<br />
              <strong>Owner Web → Outlets → Link Device</strong>
            </p>
            <form className="branch-setup-form" onSubmit={handleVerify}>
              <input
                className={`branch-code-input${status === "error" ? " error" : ""}`}
                type="text"
                value={code}
                onChange={e => { setCode(e.target.value.toUpperCase()); setStatus("idle"); }}
                placeholder="e.g. INDR-1001"
                maxLength={20}
                autoFocus
                autoComplete="off"
                spellCheck={false}
              />
              {status === "error" && <p className="branch-setup-error">⚠ {errorMsg}</p>}
              <button
                className="branch-setup-btn"
                type="submit"
                disabled={status === "loading" || !code.trim()}
              >
                {status === "loading" ? "Verifying…" : "Connect to Branch →"}
              </button>
            </form>
            <p className="branch-setup-hint">
              The link code is generated by the outlet manager. It looks like <code>ABCD-1234</code>.
            </p>
          </>
        ) : (
          <div className="branch-setup-success">
            <div className="branch-success-check">✓</div>
            <h2 className="branch-success-name">{result.outletName}</h2>
            <p className="branch-success-meta">
              {result.tables?.length || 0} tables
              {result.staff?.length ? ` · ${result.staff.length} staff` : ""}
            </p>
            <button className="branch-setup-btn success" onClick={handleConfirm}>
              Open Captain App →
            </button>
            <button
              className="branch-setup-back"
              onClick={() => { setStatus("idle"); setResult(null); setCode(""); }}
            >
              ← Wrong outlet?
            </button>
          </div>
        )}
      </div>
    </div>
  );
}

